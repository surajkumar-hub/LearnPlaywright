// How to create a function in JavaScript

// Step 1. Defining the function

function greet() {
    console.log("Hi, how are you?");
}

// Step 2. Calling the function

greet(); // O / P: Hi, how are you?
greet();
greet();
greet();
greet();
greet();
greet();

/* Something which is reusable in nature and can be called multiple times is 
called a function */