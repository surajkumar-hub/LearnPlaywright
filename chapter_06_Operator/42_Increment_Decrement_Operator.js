// Pre Increment
// Pre increment -> ++a ->. increse the value and then assign.
let a = 10;
let b = ++a; // (increment the a by 1)
console.log(b);
console.log(a);

// Post Increment
// post ++ -> first assign the value and then increase.
let a_post = 10;
let b = a_post++;
console.log(a_post);
console.log(b);

// Decrement
let a = 10;
let b = --a; // (increment the a by 1)
console.log(b); // 9
console.log(a);  // 9

let a = 10;
let b = a--; // (increment the a by 1)
console.log(b); // 10
console.log(a);  // 9

let a = 34;
let result = a++;
console.log(result);
console.log(a); 
